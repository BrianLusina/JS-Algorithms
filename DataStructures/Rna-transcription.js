function protein(rna) {
  var prot_result = "";
  var codons = rna.split(/([A-Z]{3})/);
  for(var x = 0;x < codons.length; x++){
    if(AMINO_DICT.hasOwnProperty(codons[x])){
      var h = codons[x];
      prot_result += AMINO_DICT[h];
    }
  }
 return prot_result;
}
